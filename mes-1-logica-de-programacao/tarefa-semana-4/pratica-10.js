function calcularSaldo (objeto){
    let saldo = null
    objeto.forEach(function (item) {
        if (item.movimentacao === "deposito")  
            saldo = saldo + item.valor
         else 
            saldo = saldo - item.valor
        
    });

    return `O saldo final de sua conta foi R$ ${saldo} reais.`
}

