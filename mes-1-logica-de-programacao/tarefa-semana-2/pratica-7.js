//Prática 7: Tradutor de "Hello World!"

function queIdiomaEh (idioma) {
    if (idioma === "en") {
        console.log ("Hello World!")
} else if (idioma === "es") {
        console.log("Hola mundo!")
} else {
        console.log("Olá mundo!")
}
}

queIdiomaEh ("en")
queIdiomaEh ("es")
queIdiomaEh ("pt")
queIdiomaEh (" ")

